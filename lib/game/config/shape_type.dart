/// Shape types available in the game.
///
/// Seven shape types, introduced progressively across levels:
/// - Level 1: circle, square, triangle
/// - Level 2: + pentagon
/// - Level 3: + star
/// - Level 4: + diamond
/// - Level 5: + cross
///
/// Reference: Section 4.2 -- Shape Set.
enum ShapeType {
  circle,
  square,
  triangle,
  pentagon,
  star,
  diamond,
  cross,
}
